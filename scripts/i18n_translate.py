LOOKUP = {"es": EN_ES, "de": EN_DE, "zh": EN_ZH, "fr": EN_FR}


def translate_one(en_text: str, target_lang: str) -> str:
    """Translate a single EN string to target lang. Falls back to EN if not in dict."""
    if en_text is None:
        return ""
    raw = en_text.strip()
    if not raw:
        return en_text
    key = raw.lower()
    table = LOOKUP[target_lang]
    if key in table:
        return table[key]
    # try stripping trailing punctuation
    stripped = key.rstrip(".:;!?â€¦â€”â€“-")
    if stripped in table:
        return table[stripped]
    # try with English text as-is (acronyms, technical terms)
    if raw in ("VM", "VMs", "ISO", "API", "URL", "CPU", "RAM", "DNS", "DHCP",
              "IP", "MAC", "TCP", "UDP", "TLS", "SSL", "JWT", "OAuth2",
              "SSH", "KVM", "QEMU"):
        return raw
    return en_text  # fallback


