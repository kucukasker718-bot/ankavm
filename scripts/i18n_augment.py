    en = json.load(open(f"{OUT_DIR}/en.json", encoding="utf-8"))
    es = json.load(open(f"{OUT_DIR}/es.json", encoding="utf-8"))
    de = json.load(open(f"{OUT_DIR}/de.json", encoding="utf-8"))
    zh = json.load(open(f"{OUT_DIR}/zh.json", encoding="utf-8"))
    fr = json.load(open(f"{OUT_DIR}/fr.json", encoding="utf-8"))

    # Union HTML scan + JS literal scan output so we catch hardcoded TR
    # strings in both DOM text nodes and JS toast/confirm/prompt calls.
    missing = []
    seen = set()
    for path in (f"{OUT_DIR}/missing_html_tr.txt", f"{OUT_DIR}/missing_js_tr.txt"):
        try:
            for ln in open(path, encoding="utf-8"):
                # Strip only the trailing newline; preserve in-string whitespace
                # so a JS literal like 'AI analyse error: ' stays exactly equal
                # to the key we'll write into PAGE_STRINGS.
                k = ln.rstrip("\r\n")
                if k and k not in seen:
                    seen.add(k)
                    missing.append(k)
        except FileNotFoundError:
            pass
    print(f"Augmenting with {len(missing)} TR strings...")

    added_en = 0
    added_other = 0
    for tr in missing:
        if tr in en:
            continue
        en_val = translate_tr_to_en(tr)
        en[tr] = en_val
        added_en += 1
        for lang_dict, lang_code in ((es, "es"), (de, "de"), (zh, "zh"), (fr, "fr")):
            if tr not in lang_dict:
                lang_dict[tr] = translate_one(en_val, lang_code)
                added_other += 1

    print(f"Added {added_en} EN entries, {added_other} ES/DE/ZH entries combined")

    for name, data in (("en", en), ("es", es), ("de", de), ("zh", zh), ("fr", fr)):
        json.dump(data, open(f"{OUT_DIR}/{name}_full.json", "w", encoding="utf-8"),
                  ensure_ascii=False, indent=2, sort_keys=True)
        print(f"  {name}_full.json: {len(data)} entries")


